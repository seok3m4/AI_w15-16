export type I18nKey = keyof typeof translations.ko;

export function isLocaleCode(value: string | null): value is LocaleCode {
  return value === "ko" || value === "en" || value === "zh-Hans" || value === "zh-Hant" || value === "ja";
}

export function normalizeBrowserLocale(language: string | undefined): LocaleCode {
  const normalized = (language ?? "").toLowerCase();
  if (normalized.startsWith("ko")) {
    return "ko";
  }
  if (normalized.startsWith("ja")) {
    return "ja";
  }
  if (normalized.includes("zh")) {
    if (normalized.includes("tw") || normalized.includes("hk") || normalized.includes("mo") || normalized.includes("hant")) {
      return "zh-Hant";
    }
    return "zh-Hans";
  }
  if (normalized.startsWith("en")) {
    return "en";
  }
  return DEFAULT_LOCALE;
}

export function readStoredLocale(storage: Storage | undefined = window.localStorage): LocaleCode {
  try {
    const value = storage?.getItem(LOCALE_STORAGE_KEY) ?? null;
    return isLocaleCode(value) ? value : normalizeBrowserLocale(window.navigator.language);
  } catch {
    return normalizeBrowserLocale(window.navigator.language);
  }
}

export function writeStoredLocale(locale: LocaleCode, storage: Storage | undefined = window.localStorage) {
  try {
    storage?.setItem(LOCALE_STORAGE_KEY, locale);
  } catch {
    // Storage may be unavailable in private or embedded contexts.
  }
}

export function translate(locale: LocaleCode, key: I18nKey) {
  return translations[locale][key] ?? translations.ko[key];
}
