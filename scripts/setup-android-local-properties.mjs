import { existsSync, mkdirSync, writeFileSync } from "node:fs";
import os from "node:os";
import path from "node:path";

const rootDir = process.cwd();
const androidDir = path.join(rootDir, "android");
const localPropertiesPath = path.join(androidDir, "local.properties");

function findAndroidSdk() {
  const candidates = [
    process.env.ANDROID_HOME,
    process.env.ANDROID_SDK_ROOT,
    process.platform === "win32"
      ? path.join(os.homedir(), "AppData", "Local", "Android", "Sdk")
      : "",
    process.platform === "darwin" ? path.join(os.homedir(), "Library", "Android", "sdk") : "",
    process.platform === "linux" ? path.join(os.homedir(), "Android", "Sdk") : "",
  ].filter(Boolean);

  return candidates.find((candidate) => existsSync(candidate)) ?? "";
}

function toLocalPropertiesPath(sdkPath) {
  return sdkPath.replace(/\\/g, "\\\\").replace(/:/g, "\\:");
}

if (!existsSync(androidDir)) {
  console.error(`Android project folder not found: ${androidDir}`);
  process.exit(1);
}

const sdkPath = findAndroidSdk();

if (!sdkPath) {
  console.error("Android SDK was not found.");
  console.error("Install Android Studio or set ANDROID_HOME/ANDROID_SDK_ROOT first.");
  process.exit(1);
}

mkdirSync(androidDir, { recursive: true });

const content = [
  "# Generated by npm.cmd run android:setup-local",
  "# This file is intentionally ignored by git because it contains a local SDK path.",
  `sdk.dir=${toLocalPropertiesPath(sdkPath)}`,
  "",
].join("\n");

writeFileSync(localPropertiesPath, content, "utf8");

console.log(`Wrote ${localPropertiesPath}`);
console.log(`sdk.dir=${sdkPath}`);
