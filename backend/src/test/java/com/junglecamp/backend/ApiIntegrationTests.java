	private Cookie cookieNamed(Cookie[] cookies, String name) {
		for (Cookie cookie : cookies) {
			if (name.equals(cookie.getName())) {
				return cookie;
			}
		}
		return null;
	}

	private Cookie[] appendCookie(Cookie[] cookies, Cookie extra) {
		Cookie[] appended = java.util.Arrays.copyOf(cookies, cookies.length + 1);
		appended[cookies.length] = extra;
		return appended;
	}

