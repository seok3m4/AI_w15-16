	static class FakeCaptchaVerifier implements CaptchaVerifier {

		private final java.util.Set<String> validTokens = new java.util.HashSet<>();

		@Override
		public boolean verify(String token, String remoteIp) {
			return validTokens.contains(token);
		}

		void reset() {
			validTokens.clear();
		}
	}

	static class FakeVerificationEmailSender implements VerificationEmailSender {

		private String lastEmail;
		private String lastVerificationUrl;
		private String lastVerificationCode;
		private int sentCount;

		@Override
		public void sendVerificationEmail(String email, String verificationUrl, String verificationCode) {
			lastEmail = email;
			lastVerificationUrl = verificationUrl;
			lastVerificationCode = verificationCode;
			sentCount++;
		}

		void reset() {
			lastEmail = null;
			lastVerificationUrl = null;
			lastVerificationCode = null;
			sentCount = 0;
		}
	}

	static class FakeAgentWorkerClient implements AgentWorkerClient {

		private AgentWorkerBriefingResponse nextBriefing;
		private AgentWorkerChatResponse nextChat;
		private String lastAgentId;
		private boolean failBriefing;
		private int createBriefingCalls;
		private int chatCalls;

		void reset() {
			nextBriefing = new AgentWorkerBriefingResponse(
					"Default agent briefing.",
					"verified",
					"Default Korea impact.",
					List.of("No investment advice."),
					List.of("cpi"),
					List.of(),
					List.of(),
					List.of(),
					List.of(),
					List.of(new AgentTraceStepDto("briefing-manager", "Build briefing from cached metrics.", "no-unsourced-evidence", "pass")));
			nextChat = new AgentWorkerChatResponse(
					"Default agent answer.",
					"answered",
					List.of("cpi"),
					List.of(),
					List.of(),
					List.of(),
					List.of(),
					List.of(new AgentTraceStepDto("analysis-chat", "Answer from selected briefing evidence.", "no-new-numbers", "pass")));
			lastAgentId = null;
			failBriefing = false;
			createBriefingCalls = 0;
			chatCalls = 0;
		}

		@Override
		public AgentWorkerBriefingResponse createBriefing(
				com.junglecamp.backend.economy.dto.EconomyDashboardDtos.EconomyDashboard dashboard,
				String locale) {
			createBriefingCalls++;
			if (failBriefing) {
				throw new IllegalStateException("worker unavailable");
			}
			return nextBriefing;
		}

		@Override
		public AgentWorkerChatResponse chat(
				AgentRunDetail run,
				String message,
				com.junglecamp.backend.economy.dto.EconomyDashboardDtos.EconomyDashboard dashboard,
				String toolPolicy,
				String agentId,
				String locale) {
			chatCalls++;
			lastAgentId = agentId;
			return nextChat;
		}
	}
